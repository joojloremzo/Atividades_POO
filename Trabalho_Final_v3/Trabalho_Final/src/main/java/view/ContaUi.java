    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        ArrayList<Lancamento> lancamentos = conta.getTodosLancamentos();
        ArrayList<ItemExtrato> listaExtrato = new ArrayList<>();
        double saldoAtual = 0;

        for (Lancamento lancamento : lancamentos) {
            if (lancamento instanceof Receita) {
                saldoAtual += lancamento.getValor();
                ItemExtrato extrato = new ItemExtrato(lancamento.getData(),
                        ((Receita) lancamento).getCategoriaReceita().name(),
                        lancamento.getValor(),
                        "Receita",
                        saldoAtual);
                listaExtrato.add(extrato);
            } else {
                saldoAtual -= lancamento.getValor();
                ItemExtrato extrato = new ItemExtrato(lancamento.getData(),
                        ((Despesa) lancamento).getCategoriaDespesa().name(),
                        lancamento.getValor(),
                        "Despesa",
                        saldoAtual);
                listaExtrato.add(extrato);
            }
        }

        Imprimir imprimir = new Imprimir(this, true);
        imprimir.jTextArea.setText("");

        DateTimeFormatter formatar = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        for (ItemExtrato extrato : listaExtrato) {
            String dataFormatada = extrato.getData().format(formatar);
            imprimir.jTextArea.append("Tipo: " + extrato.getTipo() + "\n");
            imprimir.jTextArea.append("Data: " + dataFormatada + "\n");
            imprimir.jTextArea.append("Valor: R$ " + String.format("%.2f", extrato.getValor()) + "\n");
            imprimir.jTextArea.append("Categoria: " + extrato.getCategoria() + "\n");
            imprimir.jTextArea.append("Saldo: " + extrato.getSaldoAcumulado() + "\n");
            imprimir.jTextArea.append("----------------------------\n");
        }
        imprimir.setVisible(true);

    }//GEN-LAST:event_jButton6ActionPerformed

