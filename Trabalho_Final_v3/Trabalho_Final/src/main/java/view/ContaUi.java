    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (file.exists()) {
            throw new IllegalArgumentException("Conta ja criada");
        }

        conta = new Conta(jtfTitular.getText());
        jtfTitular.setText("");

        try (FileWriter fw = new FileWriter(file, false); // false para sobrescrever
                 BufferedWriter bfw = new BufferedWriter(fw)) {

            // Escreve o cabeçalho da conta
            bfw.write(conta.getTitular() + ";");
            bfw.newLine();

            // Escreve o cabeçalho das colunas
            bfw.write("valor;dia;mes;ano;tipo;");
            bfw.newLine();

        } catch (IOException ex) {
            Logger.getLogger(ContaUi.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

